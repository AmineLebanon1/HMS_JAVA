    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (medID.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Please Enter Valid ID for Search");
        else {//search
            Medicine medicines = null;
            for (int i = 0; i < allMedicines.size(); i++) {
                if (id.getText().equals(allMedicines.get(i).getId() + "")) {
                    medicines = allMedicines.get(i);
                    break;
                }
            }
            if (medicines == null) {
                JOptionPane.showMessageDialog(null, "Please Enter Valid ID for Search");
            } else {
                JOptionPane.showMessageDialog(null, "Medicine Info \nName = " + medicines.getName() + "\nSelling Price = " + medicines.getSellingPrice() + "\nBuying Price = " + medicines.getBuyingPrice() + "\nQuantiity = " + medicines.getQuantity() + "\nDescription =" + medicines.getDescription());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        //if text fiels are null
        if (medID.getText().equals("") || medName.getText().equals("") || quantity.getText().equals("") || sellingPrice.getText().equals("") || buyingPrice.getText().equals("") || description.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "All Text Fields are Required!");
        } //Check if ID exists or not
        else if (checkIDandName()) {
            JOptionPane.showMessageDialog(null, "Medicine ID or Name Already Exists!");
        } else {
            try {
                Medicine medicine = new Medicine();
                medicine.setId(Integer.parseInt(medID.getText()));
                medicine.setName(medName.getText());
                medicine.setSellingPrice(Integer.parseInt(sellingPrice.getText()));
                medicine.setBuyingPrice(Integer.parseInt(buyingPrice.getText()));
                medicine.setQuantity(Integer.parseInt(quantity.getText()));
                medicine.setDescription(description.getText());
                allMedicines.add(medicine);

                medID.setText("");
                medName.setText("");
                sellingPrice.setText("");
                buyingPrice.setText("");
                quantity.setText("");
                description.setText("");
                writeAllData();
                saveAllData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "ID and Age must be integers");
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

    public boolean checkIDandName() {
        for (int i = 0; i < allMedicines.size(); i++) {
            if (medID.getText().equals(allMedicines.get(i).getId() + "")) {
                return true;
            }
        }
        return false;
    }

