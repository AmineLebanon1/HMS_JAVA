    public void readAllData() {
        try {
            File mfile = new File("mdata.txt");
            Scanner scanner = new Scanner(mfile);

            while (scanner.hasNextLine()) {
                String data = scanner.nextLine();
                //convert into array
                String[] currentData = data.split(";"); //this will return an array and store it in currentData
                //create patient object 
                Medicine medicines = new Medicine();
                medicines.setId(Integer.parseInt(currentData[0]));//id is at first index
                medicines.setName(currentData[1]);//name at 2nd index
                medicines.setSellingPrice(Integer.parseInt(currentData[2]));
                medicines.setBuyingPrice(Integer.parseInt(currentData[3]));
                medicines.setQuantity(Integer.parseInt(currentData[4]));
                medicines.setDescription(currentData[5]);
                //add medicine to arraylist
                allMedicines.add(medicines);

            }
        } catch (Exception e) {

        }
    }

    public void writeAllData() {
        allMData.setText("");
        for (int i = 0; i < allMedicines.size(); i++) {
            allMData.append(allMedicines.get(i).getId() + "  " + allMedicines.get(i).getName() + "  " + allMedicines.get(i).getSellingPrice() + "  " + allMedicines.get(i).getBuyingPrice() + "  " + allMedicines.get(i).getQuantity() + "  " + allMedicines.get(i).getDescription() + "  " + "\n");
        }
    }

